    public void populateRequestTable() {
         DefaultTableModel model = (DefaultTableModel) workRequestJTable.getModel();
        
        model.setRowCount(0);
        for (Orphanage orphanage : MainJFrameFinal.getOrphanageDirectory().getAllPendingFirstOrphanages()){
           
            Object[] row = new Object[5];
            row[0] = orphanage.getName();
            row[1] = orphanage.getUsername();
            row[2] = orphanage.getLocation();
            row[3] = orphanage.status;
           
            row[4] = orphanage.getComment();
            
            model.addRow(row);
            
        }
    }
