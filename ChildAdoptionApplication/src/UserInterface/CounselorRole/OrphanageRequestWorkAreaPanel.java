    private void btnViewOrphanageAppActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewOrphanageAppActionPerformed
        int selectedRow = workRequestJTable.getSelectedRow();

        if (selectedRow < 0){
            JOptionPane.showMessageDialog(null, "Please select a row from the table");
            return;
        }

        //  BirthMotherToCounselor request = (BirthMotherToCounselor)workRequestJTable.getValueAt(selectedRow,0);
        if (MainJFrameFinal.getOrphanageDirectory().getAllPendingFirstOrphanages().isEmpty()
            || MainJFrameFinal.getOrphanageDirectory().getAllPendingFirstOrphanages().get(selectedRow).status == Orphanage.Status.APPROVED ){
            JOptionPane.showMessageDialog(null, "Already Completed!");
            return;
        }
        else
        {
            CardLayout layout = (CardLayout) userProcessContainer.getLayout();
            userProcessContainer.add("ViewOrphanageJpanel", new ViewOrphanageJpanel(MainJFrameFinal.getOrphanageDirectory().getAllPendingFirstOrphanages().get(selectedRow), userProcessContainer));
            layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_btnViewOrphanageAppActionPerformed

